        protected void Page_Load(object sender, EventArgs e)
        {

        }
        # endregion

        # region Events
        /// <summary>
        /// Creates spreadsheet
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        protected void Button1_Click(object sender, EventArgs e)
        {
            //New instance of XlsIO is created.[Equivalent to launching Microsoft Excel with no workbooks open].
            //The instantiation process consists of two steps.

            //Step 1 : Instantiate the spreadsheet creation engine.
            ExcelEngine excelEngine = new ExcelEngine();
            //Step 2 : Instantiate the excel application object.
            IApplication application = excelEngine.Excel;

            IWorkbook workbook = application.Workbooks.Open(XlsIOHelper.ResolveApplicationDataPath(@"ExcelToODS.xlsx", Request), ExcelOpenType.Automatic);
            
            workbook.Version = ExcelVersion.Excel2016;
            workbook.SaveAs("Sample.ods", ExcelSaveType.SaveAsODS, Response, ExcelDownloadType.PromptDialog, ExcelHttpContentType.Excel2016);
 

            workbook.Close();
            excelEngine.Dispose();
        }
        # endregion
    }
}